let a = 10;
let b = 5;

//Addition
sum = a + b;
console.log("a + b = " + sum);

//Subtraction
sub = a - b;
console.log("a - b = " + sub);

//Multiplication
multi = a * b;
console.log("a * b = " + multi);

//Division
div = a / b;
console.log("a / b = " + div);

//Module
mod = a % b;
console.log("a % b = " + mod);