//Addition
function sum(num1, num2) {
  return num1 + num2;
}

//Subtraction
function sub(num1, num2) {
  return num1 - num2;
}

//Multiplication
function multi(num1, num2) {
  return num1 * num2;
}

//Division
function div(num1, num2) {
  return num1 / num2;
}

//Module
function mod(num1, num2) {
  return num1 % num2;
}

